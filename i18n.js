function createTranslator(language) {
  return (key, values = {}) => {
    let text = language === 'fr' ? (french[key] || key) : key;
    for (const [name, value] of Object.entries(values)) {
      text = text.replaceAll(`{${name}}`, String(value));
    }
    return text;
  };
}

